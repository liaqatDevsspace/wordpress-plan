// Registers block binding sources.
if ( ! function_exists( 'twentytwentyfive_register_block_bindings' ) ) :
	/**
	 * Registers the post format block binding source.
	 *
	 * @since Twenty Twenty-Five 1.0
	 *
	 * @return void
	 */
	function twentytwentyfive_register_block_bindings() {
		register_block_bindings_source(
			'twentytwentyfive/format',
			array(
				'label'              => _x( 'Post format name', 'Label for the block binding placeholder in the editor', 'twentytwentyfive' ),
				'get_value_callback' => 'twentytwentyfive_format_binding',
			)
		);
	}
endif;
add_action( 'init', 'twentytwentyfive_register_block_bindings' );

// Registers block binding callback function for the post format name.
if ( ! function_exists( 'twentytwentyfive_format_binding' ) ) :
	/**
	 * Callback function for the post format name block binding source.
	 *
	 * @since Twenty Twenty-Five 1.0
	 *
	 * @return string|void Post format name, or nothing if the format is 'standard'.
	 */
	function twentytwentyfive_format_binding() {
		$post_format_slug = get_post_format();

		if ( $post_format_slug && 'standard' !== $post_format_slug ) {
			return get_post_format_string( $post_format_slug );
		}
	}
endif;

// for localizing nonce and enqueueing js
function enqueue_book_editor_script() {
    if (is_page('update-books')) { // match slug
        wp_enqueue_script(
            'edit-books-js',
            get_template_directory_uri() . '/js/edit-books.js',
            ['jquery'],
            null,
            true
        );

        wp_localize_script('edit-books-js', 'ajax_object', [
            'ajax_url' => admin_url('admin-ajax.php'),
            'nonce'    => wp_create_nonce('update_book_nonce')
        ]);
    }
}
add_action('wp_enqueue_scripts', 'enqueue_book_editor_script');


//secure ajax handler
function handle_update_book_title() {
    check_ajax_referer('update_book_nonce', 'security');

    if (!current_user_can('edit_posts')) {
        wp_send_json_error(['message' => 'Unauthorized']);
    }

    $book_id   = intval($_POST['book_id']);
    $new_title = sanitize_text_field($_POST['new_title']);

    $post = get_post($book_id);
    if (!$post || $post->post_type !== 'book') {
        wp_send_json_error(['message' => 'Invalid book ID']);
    }

    $result = wp_update_post([
        'ID'         => $book_id,
        'post_title' => $new_title,
    ]);

    if (is_wp_error($result)) {

        wp_send_json_error(['message' => 'Failed to update']);
    }

    wp_send_json_success(['message' => 'Updated']);
}
add_action('wp_ajax_update_book_title', 'handle_update_book_title');

